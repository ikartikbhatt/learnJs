//switch statement

let day = 3;

switch (day) {
  case 0:
    console.log("day1");

    break;
  case 1:
    console.log("day2");

    break;
  case 3:
    console.log("day3");

    break;
  case 4:
    console.log("day4");

    break;
  case 5:
    console.log("day5");

    break;
  case 6:
    console.log("day6");

    break;
  case 7:
    console.log("day7");

    break;
  default:
  console.log('invalid');
    
  break;
}
